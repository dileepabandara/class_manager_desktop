                    lblsub1.setText("Bio");
                    lblsub2.setText("Chemistry");
                    lblsub3.setText("Agri");
                    lblsub11.setText("Bio");
                    lblsub22.setText("Chemistry");
                    lblsub33.setText("Agri");
