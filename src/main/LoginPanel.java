    public void selectOldPass(){
    try {
            String userId = txtRecID.getText();
            String query = "SELECT * FROM  account WHERE id = ' "+userId+" ' ";
            java.sql.Connection conn = (com.mysql.jdbc.Connection) DriverManager.getConnection("jdbc:mysql://localhost/classmanager", "root", "");
            PreparedStatement s =conn.prepareStatement(query);
            ResultSet rs = s.executeQuery(query);
            
            if (rs.next()) {
                 String oldPass = rs.getString(8);
                 txtROldPass.setText(oldPass);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }
    
    private void btnsecQconfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsecQconfirmActionPerformed
        
        
            String query = "SELECT * FROM account Where id=? and secAnswer=?";
            try{
            conn=(Connection) DriverManager.getConnection("jdbc:mysql://localhost/classmanager","root","");
            pstmt=conn.prepareStatement(query);
            pstmt.setString(1, txtRecID.getText());
            pstmt.setString(2, txtRecA.getText());
            rs=pstmt.executeQuery();
            if(rs.next()){
                JOptionPane.showMessageDialog(null, "Correct Details");
                recoverSecAns.setVisible(false);
                recoverEmail.setVisible(false);
                selectOption.setVisible(false);
                resetPass.setVisible(true);
            }else{
                JOptionPane.showMessageDialog(null, "username or password do not match");
            }

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        
            selectOldPass();
        
    }//GEN-LAST:event_btnsecQconfirmActionPerformed

