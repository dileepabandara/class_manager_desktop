    public static boolean lengthCheck(String in, int min, int max){
        return max > in.length() || in.length() > min;
    }
    
    public void validateAccountRegister(){
        String fName = txtFName.getText();
        String lName = txtLName.getText();
        String age = txtAge.getText();
        String email = txtEmail.getText();
        String mobile = txtMobile.getText();
        String className = cmbClassName.getSelectedItem().toString();
        String secQuestion = cmbSecQues.getSelectedItem().toString();
        String pass1 = txtPass1.getText();
        String secAnswer = txtSecurityAnswer.getText();
        
        String gmailPattern = "^[a-zA-Z0-9]{1,30}@[a-zA-Z0-9]{1,20}.[a-zA-Z]{2,3}$";
        Pattern pattern = Pattern.compile(gmailPattern);
        Matcher regexMatcher = pattern.matcher(email);
        
        //String EMAIL_PATTERN = "^[a-zA-Z0-9]{1,30}@[a-zA-Z0-9]{1,20}.[a-zA-Z]{2,3}$";
        
        int minLength = 10;
        int maxLength = 10;
        
        
        if (fName.equals("")) {
            JOptionPane.showMessageDialog(null, "Enter first name");
        } else if (lName.equals("")){
            JOptionPane.showMessageDialog(null, "Enter last name");
        }
        else if (age.equals("")){
            JOptionPane.showMessageDialog(null, "Enter age");
        }
        else if (email.equals("")){
            JOptionPane.showMessageDialog(null, "Enter email");
        }
        else if (!regexMatcher.matches()){
            JOptionPane.showMessageDialog(null, "Email format is not correct. Please use your gmail to register");
        }
        else if (mobile.equals("")){
            JOptionPane.showMessageDialog(null, "Enter mobile number");
        }
        else if (lengthCheck(mobile,minLength,maxLength)){
             JOptionPane.showMessageDialog(null, "Enter 10 digt mobile");
        }
        else if (className.equals("Select your class")){
            JOptionPane.showMessageDialog(null, "Select class name");
        }
        else if (pass1.equals("")){
            JOptionPane.showMessageDialog(null, "Enter password");
        }
        else if (pass1.length() <8 || pass1.length() >20) {
            JOptionPane.showMessageDialog(null, "Enter password between 8 - 20");
        }
        else if (secQuestion.equals("Select your question")){
            JOptionPane.showMessageDialog(null, "Select seurity question");
        }
        else if (secAnswer.equals("")){
            JOptionPane.showMessageDialog(null, "Enter answer");
        }
        else {
            JOptionPane.showMessageDialog(null, "No mistakes");
            createAccount();
            Home h2 = new Home();
            h2.setVisible(true);
            this.dispose();
        }
        
    }
   
    
